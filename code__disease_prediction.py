plt.ylabel("Feature")
plt.tight_layout()
plt.savefig("feature_importance_rf.png", dpi=150)
plt.show()
print("Saved: feature_importance_rf.png")

# XGBoost Feature Importance
xgb_importance = pd.Series(xgb_model.feature_importances_, index=feature_names)
xgb_importance = xgb_importance.sort_values(ascending=False)

plt.figure(figsize=(10, 6))
sns.barplot(x=xgb_importance.values, y=xgb_importance.index, palette="Oranges_r")
plt.title("Feature Importance - XGBoost")
plt.xlabel("Importance Score")
plt.ylabel("Feature")
plt.tight_layout()
plt.savefig("feature_importance_xgb.png", dpi=150)
plt.show()
print("Saved: feature_importance_xgb.png")

from sklearn.metrics import confusion_matrix

fig, axes = plt.subplots(1, 3, figsize=(18, 5))

models = [("Logistic Regression", lr_model),
          ("Random Forest",       rf_model),
          ("XGBoost",             xgb_model)]

for ax, (name, model) in zip(axes, models):
    y_pred = model.predict(X_test)
    cm = confusion_matrix(y_test, y_pred)

    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues",
                xticklabels=["No Disease", "Disease"],
                yticklabels=["No Disease", "Disease"],
                ax=ax)
    ax.set_title(f"Confusion Matrix\n{name}")
    ax.set_xlabel("Predicted Label")
    ax.set_ylabel("True Label")

plt.tight_layout()
plt.savefig("confusion_matrix.png", dpi=150)
plt.show()
print("Saved: confusion_matrix.png")

# Create comparison dataframe
results = pd.DataFrame({
    "Model": ["Logistic Regression", "Random Forest", "XGBoost"],
    "Accuracy":  [lr_scores[0],  rf_scores[0],  xgb_scores[0]],
    "Precision": [lr_scores[1],  rf_scores[1],  xgb_scores[1]],
    "Recall":    [lr_scores[2],  rf_scores[2],  xgb_scores[2]],
    "F1-Score":  [lr_scores[3],  rf_scores[3],  xgb_scores[3]],
    "ROC-AUC":   [lr_scores[4],  rf_scores[4],  xgb_scores[4]],
})

print(results)

# Plot comparison
results_melted = results.melt(id_vars="Model", var_name="Metric", value_name="Score")

plt.figure(figsize=(12, 6))
sns.barplot(x="Metric", y="Score", hue="Model", data=results_melted, palette="Blues")
plt.title("Model Comparison — All Metrics")
plt.xlabel("Metric")
plt.ylabel("Score")
plt.ylim(0.7, 1.05)
plt.legend(loc="lower right")
plt.tight_layout()
plt.savefig("model_comparison.png", dpi=150)
plt.show()
print("Saved: model_comparison.png")

import os
import pickle

# Find the best model based on ROC-AUC score
model_scores = {
    "Logistic Regression": lr_scores[4],
    "Random Forest":       rf_scores[4],
    "XGBoost":             xgb_scores[4],
}

best_model_name = max(model_scores, key=model_scores.get)
print(f"Best Model: {best_model_name} (ROC-AUC: {model_scores[best_model_name]*100:.2f}%)")

# Save the best model
os.makedirs("model", exist_ok=True)

if best_model_name == "Logistic Regression":
    best_model = lr_model
elif best_model_name == "Random Forest":
    best_model = rf_model
else:
    best_model = xgb_model

# Save model and scaler
with open("model/best_model.pkl", "wb") as f:
    pickle.dump(best_model, f)

with open("model/scaler.pkl", "wb") as f:
    pickle.dump(scaler, f)

print("Best model saved to model/best_model.pkl ")
print("Scaler saved to model/scaler.pkl ")

